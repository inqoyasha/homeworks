package org.azamat.homeworks.OOP.myTriangle;

public class MyTriangle {
    private MyPoint v1;
    private MyPoint v2;
    private MyPoint v3;

    public MyTriangle(int x1, int y1, int x2, int y2, int x3, int y3) {
        v1.x = x1;
        v1.y = y1;
        v2.x = x2;
        v2.y = y2;
        v3.x = x3;
        v3.y = y3;
    }
}
